public class Apple {
    private int weight = 0;
    private String color = "";
    public Apple(int weight, String color) {
        this.weight = weight;
        this.color = color;
    }
    public Integer getWeight() {
        return weight;
    }
    public String getColor() {
        return color;
    }
}
